def get_persona(key: str) -> dict:
    return PERSONAS.get(key, PERSONAS["A"])


def persona_system_prompt(key: str) -> str:
    p = get_persona(key)
    return p["system"]


def persona_options() -> list[str]:
    return list(PERSONAS.keys())


def persona_labels() -> dict[str, str]:
    return {k: v["label"] for k, v in PERSONAS.items()}
